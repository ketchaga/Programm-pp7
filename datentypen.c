#include <stdio.h>
#include <stdbool.h>

int main() {
    int num = 10;
    float decimal = 3.14;
    char letter = 'A';
    bool flag = true;

    printf("Integer: %d\n", num);
    printf("Float: %f\n", decimal);
    printf("Character: %c\n", letter);
    printf("Boolean: %s\n", flag ? "true" : "false");

    return 0;
}


